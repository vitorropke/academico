dimensao_1=float(input("Digite a primeira dimensão: "))
dimensao_2=float(input("Digite a segunda dimensão: "))

area=dimensao_1*dimensao_2

if area>=600:
    print("Esta área é suficiente para começar a construir.")
else:
    print("Esta área é insuficiente para começar a construir. Convém aumentar esta área.")
